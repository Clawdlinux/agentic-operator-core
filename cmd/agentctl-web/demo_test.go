func TestHandleDemoRendersEvidenceConsole(t *testing.T) {
	server, err := NewServer(nil, nil, TemplatesFS())
	if err != nil {
		t.Fatalf("NewServer() error = %v", err)
	}

	req := httptest.NewRequest(http.MethodGet, "/demo", nil)
	res := httptest.NewRecorder()

	server.handleDemo(res, req)

	if res.Code != http.StatusOK {
		t.Fatalf("status = %d, want %d", res.Code, http.StatusOK)
	}

	body := res.Body.String()
	for _, want := range []string{
		`href="/theme/clawdlinux-theme.css"`,
		`src="/theme/clawdlinux-wordmark.svg"`,
		`alt="Clawdlinux"`,
		"Governance evidence console",
		">LIVE</span>",
		">CONFIG ONLY</span>",
		">PRIOR RUN</span>",
		"No OPA evidence",
		"No scheduled gVisor pod",
		"No packet enforcement test",
		"gVisor admission dry-run",
		"NetworkPolicy object presence",
		"Current workload generated no audit fixture",
		"no pod or packet test",
		"not generated by the current workload",
		`hx-trigger="every 5s"`,
	} {
		if !strings.Contains(body, want) {
			t.Fatalf("demo body missing %q", want)
		}
	}

	sharedThemeIndex := strings.Index(body, `href="/theme/clawdlinux-theme.css"`)
	localStyleIndex := strings.Index(body, `href="/static/style.css"`)
	if sharedThemeIndex == -1 || localStyleIndex == -1 || sharedThemeIndex > localStyleIndex {
		t.Fatal("shared theme must load before local styles")
	}
	if count := strings.Count(body, "OPA"); count != 1 {
		t.Fatalf("demo body contains %d OPA references, want disclaimer only", count)
	}

	for _, stale := range []string{
		"OPA checks tool scope",
		"Policy gated",
		"gpt-4o-mini",
		"Secure Research Swarm",
		"$0.0142",
		"Audit trail",
		"Audit ready",
		"Current audit trail",
		"Current attestation",
		"Attestation verified",
		"3 agents",
		"No external data egress",
		"AgentWorkload running",
		"Live agent graph",
		"Agent graph",
		"Agentic Operator",
	} {
		if strings.Contains(body, stale) {
			t.Fatalf("demo body contains stale claim %q", stale)
		}
	}

	for label, want := range map[string]int{
		">LIVE</span>":        1,
		">CONFIG ONLY</span>": 1,
		">PRIOR RUN</span>":   1,
	} {
		if count := strings.Count(body, label); count != want {
			t.Errorf("demo body contains %d %s labels, want %d", count, label, want)
		}
	}
}

